import * as moment from 'moment';
import { Plugin } from '../model/plugin';

export const videoPlugin: Plugin = {
  tag: 'plugin/video',
  name: 'Video Plugin',
  config: {
    generated: 'Generated by jenkins-ui ' + moment().toISOString(),
  },
  defaults: {},
  schema: {
    optionalProperties: {
      url: { type: 'string' },
    },
  },
};

export const videoExtensions = ['.3gp', '.mpg', '.mpeg', '.mp4', '.m4v', '.m4p', '.webm', '.ogv', '.m3u8', '.mov', '.ts'];

export function isVideo(url: string) {
  return videoExtensions.includes(url.slice(url.lastIndexOf('.')).toLowerCase());
}
